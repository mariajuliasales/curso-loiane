package mariajulia.cursojava.aula11;

public class Curiosidade {

	public static void main(String[] args) {

		int var1 = 2147483647;
		int var2 = 1;

		double var3 = 0.2e2;

		// 0,2 * 10²

		System.out.println(var1 + var2);

		// intervalo de valores de int é de -2147483648 a 2147483647
		// se ultrapassar esse valor maximo, volta para o valor minimo e assim vai
		System.out.println(var3);
		System.out.println(0b11010);
	}

}
